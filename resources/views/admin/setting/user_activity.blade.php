<script>
    $(function() {
        loadDataTable();
    });

    function exportExcel(){
        var search = window.table.search();
        var item = $('#item_id').val() ? $('#item_id').val():'';
        var inventory_type = $('#filter_type').val();
        window.location = "{{ Request::url() }}/export?search=" + search + "&item=" + item+"&inventory_type=" + inventory_type;
    }

    function loadDataTable() {
		window.table = $('#datatable_serverside').DataTable({
            "responsive": false,
            "scrollX": true,
            "stateSave": true,
            "serverSide": true,
            "deferRender": true,
            "destroy": true,
            "iDisplayInLength": 10,
            "order": [[0, 'desc']],
            dom: 'Blfrtip',
            buttons: [
                'columnsToggle',
            ],
            "language": {
                "lengthMenu": "Menampilkan _MENU_ data per halaman",
                "zeroRecords": "Data tidak ditemukan / kosong",
                "info": "Menampilkan halaman _PAGE_ / _PAGES_ dari total _TOTAL_ data",
                "infoEmpty": "Data tidak ditemukan / kosong",
                "infoFiltered": "(disaring dari _MAX_ total data)",
                "search": "Cari",
                "paginate": {
                    first:      "<<",
                    previous:   "<",
                    next:       ">",
                    last:       ">>"
                },
                "select": {
                    rows: "%d baris terpilih"
                }
            },
            ajax: {
                url: '{{ Request::url() }}/datatable',
                type: 'GET',
                data: {
                    inventory_type : $('#filter_type').val(),
                    item : $('#item').val(),
                },
                beforeSend: function() {
                    loadingOpen('#datatable_serverside');
                },
                complete: function() {
                    loadingClose('#datatable_serverside');
                },
                error: function() {
                    loadingClose('#datatable_serverside');
                    swal({
                        title: 'Ups!',
                        text: 'Check your internet connection.',
                        icon: 'error'
                    });
                }
            },
            columns: [
                { name: 'id', searchable: false, className: 'center-align' },
                { name: 'causer_id', searchable: false, orderable: false, className: '' },
                { name: 'description', searchable: false, orderable: false, className: '' },
                { name: 'form', searchable: false, orderable: false, className: '' },
                { name: 'data', searchable: false, orderable: false, className: '' },
                { name: 'updated_at', searchable: false, className: '' },
            ],
        });
        $('.dt-buttons').appendTo('#datatable_buttons');

        $('select[name="datatable_serverside_length"]').addClass('browser-default');
	}

</script>